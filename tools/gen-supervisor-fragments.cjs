#!/usr/bin/env node
//
// Regenerate the supervisord program fragments used by the supervised backend
// container, from the docker-compose.yml backend service commands.
//
//   node tools/gen-supervisor-fragments.cjs        # writes the fragment files
//   node tools/gen-supervisor-fragments.cjs --check # verify they are up to date (CI/pre-commit)
//
// Output:
//   docker/legacy-portal/supervisor/conf.d/pollers.conf      (poller role: monitor + hybrid)
//   docker/legacy-portal/supervisor/conf.d/aggregators.conf  (aggregator role: master + hybrid)
//   docker/legacy-portal/supervisor/conf.d/minimal-hybrid.programs.conf
//   docker/legacy-portal/supervisor/conf.d/minimal-monitor.programs.conf
//
// Classification mirrors the code-enforced split (@require_backend_roles in
// sdcc/sdcc/monitor/async_stats_collector/scripts/app_runners.py): a service is an
// AGGREGATOR if its name ends with "-aggregator" or is incident-manager /
// dp-policy-manager; otherwise it is a POLLER (incl. cmd-executor, netflow-manager).
//
// pollers.conf / aggregators.conf carry autostart=true and are included by the
// full-* profiles. The minimal-*.programs.conf fragments contain the SAME program
// set as their full counterpart (minimal-hybrid = pollers+aggregators, like
// full-hybrid; minimal-monitor = pollers, like full-monitor) but autostart=true
// ONLY for the minimal subset below — every other program is present (visible &
// startable via supervisorctl) with autostart=false. This is why each program is
// declared exactly once per fragment (no duplicate-section overrides).
//
// The profile files (minimal-hybrid.conf, full-hybrid.conf, …) are hand-maintained
// and only [include] these generated fragments — they are NOT touched here.

const fs = require('fs');
const path = require('path');

const composePath = path.resolve(__dirname, '../docker/legacy-portal/docker-compose.yml');
const confDir = path.resolve(__dirname, '../docker/legacy-portal/supervisor/conf.d');

const AGG_EXTRA = new Set(['incident-manager', 'dp-policy-manager']);
const isAggregator = (svc) => svc.endsWith('-aggregator') || AGG_EXTRA.has(svc);

// Programs auto-started by each minimal profile. Everything else in the fragment
// is still defined (visible/startable) but autostart=false.
const MINIMAL_HYBRID = new Set(['cmd-executor', 'incident-manager']);
const MINIMAL_MONITOR = new Set(['cmd-executor']);

function programBlock(name, innerCmd, autostart) {
  return [
    '',
    `[program:${name}]`,
    `command=/bin/sh -c "${innerCmd}"`,
    `autostart=${autostart ? 'true' : 'false'}`,
    'autorestart=unexpected',
    'startsecs=5',
    'stdout_logfile=/dev/stdout',
    'stdout_logfile_maxbytes=0',
    'stderr_logfile=/dev/stderr',
    'stderr_logfile_maxbytes=0',
  ].join('\n');
}

function parse() {
  const lines = fs.readFileSync(composePath, 'utf8').split(/\r?\n/);
  const svcRe = /^  ([A-Za-z0-9_-]+):\s*$/;     // top-level service header (2-space indent)
  const innerRe = /"([^"]*)"\]\s*$/;             // last quoted arg before "]  at line end
  const pollers = [];
  const aggregators = [];
  let svc = null;
  for (const line of lines) {
    const m = line.match(svcRe);
    if (m) { svc = m[1]; continue; }
    if (svc && line.includes('command:') && line.includes('exec sdcc-')) {
      const im = line.match(innerRe);
      if (!im) { console.error(`WARN: could not parse command for ${svc}: ${line.trim()}`); continue; }
      (isAggregator(svc) ? aggregators : pollers).push({ name: svc, innerCmd: im[1] });
    }
  }
  return { pollers, aggregators };
}

// Render a fragment from {name, innerCmd} programs. `autostartOf(name)` decides
// the autostart flag per program (full fragments pass () => true).
function render(header, programs, autostartOf) {
  return [
    '; AUTO-GENERATED by tools/gen-supervisor-fragments.cjs from docker-compose.yml.',
    `; ${header}`,
    '; Do not edit by hand — re-run the generator after changing compose backend services.',
    ...programs.map((p) => programBlock(p.name, p.innerCmd, autostartOf(p.name))),
    '',
  ].join('\n');
}

function main() {
  const check = process.argv.includes('--check');
  const { pollers, aggregators } = parse();
  const ALWAYS = () => true;
  const targets = [
    [path.join(confDir, 'pollers.conf'), render('Poller-role programs (monitor + hybrid).', pollers, ALWAYS)],
    [path.join(confDir, 'aggregators.conf'), render('Aggregator-role programs (master + hybrid).', aggregators, ALWAYS)],
    // Minimal program sets: same programs as the full counterpart, but only the
    // minimal subset auto-starts; the rest are present with autostart=false.
    [path.join(confDir, 'minimal-hybrid.programs.conf'),
      render('Minimal-hybrid set (pollers+aggregators); autostart only: ' + [...MINIMAL_HYBRID].join(', ') + '.',
        [...pollers, ...aggregators], (n) => MINIMAL_HYBRID.has(n))],
    [path.join(confDir, 'minimal-monitor.programs.conf'),
      render('Minimal-monitor set (pollers); autostart only: ' + [...MINIMAL_MONITOR].join(', ') + '.',
        pollers, (n) => MINIMAL_MONITOR.has(n))],
  ];

  if (check) {
    let stale = false;
    for (const [file, content] of targets) {
      const cur = fs.existsSync(file) ? fs.readFileSync(file, 'utf8') : '';
      if (cur !== content) { stale = true; console.error(`STALE: ${path.relative(process.cwd(), file)}`); }
    }
    if (stale) { console.error('Run: npm run gen-supervisor-fragments'); process.exit(1); }
    console.log(`up to date (pollers: ${pollers.length}, aggregators: ${aggregators.length})`);
    return;
  }

  fs.mkdirSync(confDir, { recursive: true });
  for (const [file, content] of targets) fs.writeFileSync(file, content);
  console.log(`pollers: ${pollers.length}  aggregators: ${aggregators.length}`);
  console.log(`wrote ${path.relative(process.cwd(), confDir)}/{pollers,aggregators}.conf`);
}

main();
